function builtInSuggest(
  query: string,
  level: string,
  region?: string,
  district?: string,
): AddressSuggestion[] {
  const q = query.trim().toLowerCase();
  if (!q || q.length < 2) return [];

  const results: AddressSuggestion[] = [];

  if (level === "district") {
    // Filter districts in the given region (or all regions if none specified)
    const sources = region && DISTRICTS_BY_REGION[region]
      ? { [region]: DISTRICTS_BY_REGION[region] }
      : DISTRICTS_BY_REGION;

    for (const [reg, dists] of Object.entries(sources)) {
      for (const dist of dists) {
        if (dist.toLowerCase().includes(q)) {
          results.push({
            label: dist,
            value: dist,
            fiasId: null,
            level,
            type: "район",
            region: reg,
            district: dist,
            locality: null,
            fullAddress: `${dist} район, ${reg}`,
          });
          if (results.length >= 10) break;
        }
      }
      if (results.length >= 10) break;
    }
  } else if (level === "locality" || level === "territory") {
    // If district known — search district cities first
    if (district && CITIES_BY_DISTRICT[district]) {
      for (const city of CITIES_BY_DISTRICT[district]) {
        if (city.toLowerCase().includes(q)) {
          results.push({
            label: city,
            value: city,
            fiasId: null,
            level,
            type: "населённый пункт",
            region: region ?? null,
            district,
            locality: city,
            fullAddress: `${city}, ${district} район, ${region ?? ""}`.trim().replace(/, $/, ""),
          });
          if (results.length >= 10) break;
        }
      }
    }

    // Also search region-level cities
    if (results.length < 10) {
      const regionCities = region && CITIES_BY_REGION[region]
        ? CITIES_BY_REGION[region]
        : Object.values(CITIES_BY_REGION).flat();

      for (const city of regionCities) {
        if (city.toLowerCase().includes(q) && !results.some(r => r.label === city)) {
          results.push({
            label: city,
            value: city,
            fiasId: null,
            level,
            type: "город",
            region: region ?? null,
            district: district ?? null,
            locality: city,
            fullAddress: city,
          });
          if (results.length >= 10) break;
        }
      }
    }
  } else if (level === "region") {
    const allRegions = Object.keys(DISTRICTS_BY_REGION);
    for (const reg of allRegions) {
      if (reg.toLowerCase().includes(q)) {
        results.push({
          label: reg,
          value: reg,
          fiasId: null,
          level,
          type: "субъект РФ",
          region: reg,
          district: null,
          locality: null,
          fullAddress: reg,
        });
        if (results.length >= 10) break;
      }
    }
  } else if (level === "street") {
    for (const street of MOCK_STREETS) {
      if (street.toLowerCase().includes(q)) {
        results.push({
          label: street,
          value: street,
          fiasId: null,
          level,
          type: "улица",
          region: region ?? null,
          district: district ?? null,
          locality: null,
          fullAddress: street,
        });
        if (results.length >= 10) break;
      }
    }
  } else {
    for (const street of MOCK_STREETS) {
      if (street.toLowerCase().includes(q)) {
        results.push({
          label: street,
          value: street,
          fiasId: null,
          level,
          type: "улица",
          region: region ?? null,
          district: district ?? null,
          locality: null,
          fullAddress: street,
        });
        if (results.length >= 8) break;
      }
    }
  }

  return results.slice(0, 10);
}

// ─── Route ────────────────────────────────────────────────────────────────────

router.get("/address/suggest", async (req, res) => {
  const {
    query,
    level = "address",
    region,
    district,
    parentId,
  } = req.query as {
    query: string;
    level?: string;
    region?: string;
    district?: string;
    parentId?: string;
  };

  if (!query || query.trim().length < 2) {
    res.json([]);
    return;
  }

  if (!DADATA_API_KEY) {
    // Use comprehensive built-in database (all 85 regions) — works in any environment
    res.json(builtInSuggest(query.trim(), level, region, district));
    return;
  }

  try {
    const results = await dadataSuggest(query.trim(), level, region, district, parentId);
    res.json(results);
  } catch (err) {
    logger.error({ err }, "Dadata suggest error — falling back to built-in database");
    res.json(builtInSuggest(query.trim(), level, region, district));
  }
});

export default router;
