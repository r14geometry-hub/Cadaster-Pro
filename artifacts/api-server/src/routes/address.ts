function mockSuggest(query: string, level: string, region?: string): Suggestion[] {
  const q = query.trim().toLowerCase();
  if (!q || q.length < 2) return [];

  const results: Suggestion[] = [];

  if (level === "district") {
    const sources = region && MOCK_DATA[region] ? { [region]: MOCK_DATA[region] } : MOCK_DATA;
    for (const [reg, data] of Object.entries(sources)) {
      for (const district of data.districts) {
        if (district.toLowerCase().includes(q)) {
          results.push({ value: district, district, locality: null, region: reg });
          if (results.length >= 10) break;
        }
      }
      if (results.length >= 10) break;
    }
  } else if (level === "locality") {
    const sources = region && MOCK_DATA[region] ? { [region]: MOCK_DATA[region] } : MOCK_DATA;
    for (const [reg, data] of Object.entries(sources)) {
      for (const [district, localities] of Object.entries(data.localities)) {
        for (const locality of localities) {
          if (locality.toLowerCase().includes(q)) {
            results.push({ value: locality, district, locality, region: reg });
            if (results.length >= 10) break;
          }
        }
        if (results.length >= 10) break;
      }
      if (results.length >= 10) break;
    }
    // Also search regional centres
    if (results.length < 10) {
      const CENTRES: Record<string, string> = {
        "Москва": "Москва", "Санкт-Петербург": "Санкт-Петербург",
        "Московская область": "Красногорск", "Краснодарский край": "Краснодар",
        "Новосибирская область": "Новосибирск", "Свердловская область": "Екатеринбург",
        "Нижегородская область": "Нижний Новгород", "Самарская область": "Самара",
        "Челябинская область": "Челябинск", "Республика Татарстан": "Казань",
        "Ростовская область": "Ростов-на-Дону", "Республика Башкортостан": "Уфа",
        "Иркутская область": "Иркутск",
      };
      const filterReg = region ? { [region]: CENTRES[region] } : CENTRES;
      for (const [reg, centre] of Object.entries(filterReg)) {
        if (centre && centre.toLowerCase().includes(q) && !results.some(r => r.locality === centre)) {
          results.push({ value: centre, district: null, locality: centre, region: reg });
          if (results.length >= 10) break;
        }
      }
    }
  } else {
    // address level — combine region centre + street hints
    const streetPrefixes = ["ул.", "пр.", "пер.", "бул.", "пл.", "наб.", "ш.", "пр-т"];
    const streets = ["Ленина", "Мира", "Советская", "Центральная", "Садовая", "Победы", "Молодёжная", "Школьная", "Лесная", "Набережная", "Кирова", "Гагарина", "Пушкина", "Октябрьская", "Полевая"];
    for (const prefix of streetPrefixes) {
      for (const street of streets) {
        const full = `${prefix} ${street}`;
        if (full.toLowerCase().includes(q) || street.toLowerCase().includes(q)) {
          results.push({ value: full, district: null, locality: null, region: region ?? null });
          if (results.length >= 8) break;
        }
      }
      if (results.length >= 8) break;
    }
  }

  return results.slice(0, 10);
}

async function dadataSuggest(query: string, level: string, region?: string): Promise<Suggestion[]> {
  const locationsConstraint = region ? [{ region }] : [];

  const fromBound = level === "district" ? "area" : level === "locality" ? "city" : "street";
  const toBound = level === "district" ? "area" : level === "locality" ? "settlement" : "house";

  const body = {
    query,
    count: 10,
    from_bound: { value: fromBound },
    to_bound: { value: toBound },
    ...(locationsConstraint.length > 0 ? { locations: locationsConstraint } : {}),
  };

  const resp = await fetch("https://suggestions.dadata.ru/suggestions/api/4_1/rs/suggest/address", {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      "Authorization": `Token ${DADATA_API_KEY}`,
    },
    body: JSON.stringify(body),
  });

  if (!resp.ok) throw new Error(`Dadata error: ${resp.status}`);

  const data = await resp.json() as { suggestions: Array<{ value: string; data: { area?: string; city?: string; settlement?: string; region?: string } }> };

  return data.suggestions.map((s) => ({
    value: s.value,
    district: s.data.area ?? null,
    locality: s.data.city ?? s.data.settlement ?? null,
    region: s.data.region ?? null,
  }));
}

router.get("/address/suggest", async (req, res) => {
  const { query, level = "address", region } = req.query as { query: string; level?: string; region?: string };

  if (!query || query.length < 2) {
    res.json([]);
    return;
  }

  try {
    let results: Suggestion[];

    if (DADATA_API_KEY) {
      results = await dadataSuggest(query, level, region);
    } else {
      results = mockSuggest(query, level, region);
    }

    res.json(results);
  } catch (err) {
    logger.warn({ err }, "Address suggest error — falling back to mock");
    res.json(mockSuggest(query, level, region));
  }
});

